import React, { useRef, useEffect } from 'react';
import * as THREE from 'three';
import { OrbitControls } from 'three/examples/jsm/controls/OrbitControls';

const Canvas3D = ({ imageUrl }) => {
    const mountRef = useRef(null);

    useEffect(() => {
        // Scene, Camera, Renderer setup
        const scene = new THREE.Scene();
        scene.background = new THREE.Color(0xffffff); // Set background color to white for better visibility

        const camera = new THREE.PerspectiveCamera(75, window.innerWidth / window.innerHeight, 0.1, 1000);
        camera.position.z = 2; // Adjusted to ensure the plane is within the view frustum

        const renderer = new THREE.WebGLRenderer({ antialias: true });
        renderer.setSize(window.innerWidth, window.innerHeight);
        mountRef.current.appendChild(renderer.domElement);

        // Load Plane with Height Map from PNG Texture
        const geometry = new THREE.PlaneGeometry(1, 1, 512, 512); // Increased subdivisions for more detail
        const textureLoader = new THREE.TextureLoader();

        // Debugging: Check if the image URL is correct
        if (!imageUrl) {
            console.error('No image URL provided!');
            return;
        }

        console.log('Loading texture from:', imageUrl);

        // Load the texture and create height map
        const texture = textureLoader.load(
            imageUrl,
            (texture) => {
                console.log('Texture loaded successfully');
                
                // Create a height map from the texture
                const canvas = document.createElement('canvas');
                const context = canvas.getContext('2d');
                canvas.width = texture.image.width;
                canvas.height = texture.image.height;
                context.drawImage(texture.image, 0, 0);

                const imageData = context.getImageData(0, 0, canvas.width, canvas.height);
                const { data } = imageData;

                // Displace the geometry vertices based on grayscale pixel brightness
                const positions = geometry.attributes.position.array;
                for (let i = 0; i < positions.length; i += 3) {
                    const x = (i / 3) % 512; // x coordinate in the image
                    const y = Math.floor((i / 3) / 512); // y coordinate in the image
                    const pixelIndex = (y * canvas.width + x) * 4; // RGBA, so we multiply by 4

                    // Calculate grayscale value from RGB
                    const r = data[pixelIndex];
                    const g = data[pixelIndex + 1];
                    const b = data[pixelIndex + 2];
                    const gray = (r + g + b) / 3; // Average to get grayscale

                    const height = gray / 255; // Normalize to [0, 1]
                    positions[i + 2] = height * 0.3; // Scale height by 0.3
                }

                geometry.computeVertexNormals(); // Recompute normals for lighting

                const material = new THREE.MeshStandardMaterial({
                    map: texture, // Apply the image as a texture
                    displacementMap: texture, // Use the image as a displacement map
                    displacementScale: 0.3, // Control displacement intensity
                    wireframe: false,
                });

                const plane = new THREE.Mesh(geometry, material);
                scene.add(plane);

                // Start animation loop once the geometry is set up
                animate();
            },
            undefined, // onProgress (not used)
            (error) => console.error('Error loading texture:', error) // Error callback
        );

        // Orbit Controls for user interaction
        const controls = new OrbitControls(camera, renderer.domElement);
        controls.enableDamping = true; // Smooth controls
        controls.dampingFactor = 0.25;
        controls.enableZoom = true; // Enable zooming
        controls.enableRotate = true; // Enable rotating
        controls.enablePan = true; // Enable panning

        // Add a light source to see the displacement effect
        const ambientLight = new THREE.AmbientLight(0xffffff, 0.5); // Soft white light
        scene.add(ambientLight);

        const directionalLight = new THREE.DirectionalLight(0xffffff, 1); // Strong white light
        directionalLight.position.set(2, 2, 5).normalize();
        scene.add(directionalLight);

        // Animation Loop
        const animate = () => {
            requestAnimationFrame(animate);
            controls.update(); // Required for damping
            renderer.render(scene, camera);
        };

        // Handle Resize
        const handleResize = () => {
            renderer.setSize(window.innerWidth, window.innerHeight);
            camera.aspect = window.innerWidth / window.innerHeight;
            camera.updateProjectionMatrix();
        };
        window.addEventListener('resize', handleResize);

        // Clean Up on Unmount
        return () => {
            window.removeEventListener('resize', handleResize);
            mountRef.current.removeChild(renderer.domElement);
        };
    }, [imageUrl]); // Re-run effect if the image URL changes

    return <div ref={mountRef} className="w-full h-full" />;
};

export default Canvas3D;






